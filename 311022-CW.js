// Calculate the sum of the numbers in the nth row of this triangle (starting at index 1) 

// integers, positive, 
// sum of numbers on row, n * n * n

function rowSumOddNumbers(n) {
	return n * n * n
}

console.log(rowSumOddNumbers(3)) // 27 - 3 * 3 = 9 * 3 = 27
console.log(rowSumOddNumbers(2)) // 8 - 2*2 = 4 * 2 = 8